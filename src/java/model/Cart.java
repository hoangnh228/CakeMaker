package model;

/**
 *
 * @author cam
 */
public class Cart {
    private int id;
    private int quantity;
    private String title;
    private float price;
    private String image;

    public Cart() {
    }

    public Cart(int id, int quantity, String title, float price, String image) {
        this.id = id;
        this.quantity = quantity;
        this.title = title;
        this.price = price;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
}
